package Math;
//https://leetcode.com/problems/add-two-integers/description/
// time complexity: O(1)
// space complexity: O(1)
// approach: we can simply return the sum of the two integers.
// technique used: math
class  AddTwoIntegers{
    public int sum(int num1, int num2) {
        return num1+num2;
    }
}